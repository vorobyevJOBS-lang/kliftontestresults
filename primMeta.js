// Уровни: −100…+100 → 5 уровней
export function getPrimLevel(score) {
  if (score >= 60)  return "Очень высокий";
  if (score >= 25)  return "Высокий";
  if (score >= -25) return "Средний";
  if (score >= -60) return "Низкий";
  return "Очень низкий";
}

