// Расчёт соответствия профилю (0–100%)
export function calcJobMatch(scores, profile) {
  const entries = Object.entries(profile.requirements);
  if (!entries.length) return 0;

  let totalMatch = 0;
  entries.forEach(([factorId, threshold]) => {
    const factorName = PRIM_SCALE_TO_NAME[factorId];
    const score = scores[factorName] ?? 0;
    // Normalize: threshold is minimum needed. If score >= threshold → 100%
    // If score < threshold → proportional, capped at 0%
    const normalized = Math.max(0, Math.min(1, (score + 100) / (threshold + 100)));
    totalMatch += normalized;
  });

  return Math.round((totalMatch / entries.length) * 100);
}
