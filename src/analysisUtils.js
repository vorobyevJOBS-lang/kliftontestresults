export function sailsAnalysis(scales) {
  const entries = Object.entries(scales).map(([k, v]) => [k, Number(v)]);
  const pros = entries.filter(([, s]) => s >= 63).map(([k]) => SAILS_PROS_TEXT[k]).filter(Boolean);
  const cons = entries.filter(([, s]) => s <= 41).map(([k]) => SAILS_CONS_TEXT[k]).filter(Boolean);

  const sorted = [...entries].sort((a, b) => b[1] - a[1]);
  const top3 = sorted.slice(0, 3).map(([k, v]) => `${SAILS_NAMES[k]} (${v})`).join(", ");
  const bot3 = sorted.slice(-3).reverse().map(([k, v]) => `${SAILS_NAMES[k]} (${v})`).join(", ");
  const avg = Math.round(entries.reduce((a, [, v]) => a + v, 0) / entries.length);

  let verdict;
  if (avg >= 70) {
    verdict = `Сильный профиль продавца (средний балл ${avg}). Топ-компетенции: ${top3}. Рекомендован для позиции в продажах.`;
  } else if (avg >= 50) {
    verdict = `Хороший потенциал (средний балл ${avg}). Сильные стороны: ${top3}. Зоны роста: ${bot3}.`;
  } else if (avg >= 35) {
    verdict = `Средний профиль (средний балл ${avg}). Требует развития по ключевым компетенциям: ${bot3}.`;
  } else {
    verdict = `Низкий профиль продавца (средний балл ${avg}). Слабые компетенции: ${bot3}. Не рекомендован без дополнительной проверки.`;
  }

  return { pros, cons, verdict };
}

// ── РЕЗАЛТ ───────────────────────────────────────────────────
// Классификация по методике «три корзины»:
// ВИННЕР: видит проблему → находит решение → воплощает → самостоятельно
// ДУЕР:   3 из 4 качеств — делает, но нужна подсказка или разрешение
// ВЭЙТЕР: нет желания работать, избегает ответственности

function classifyJob(job) {
  // job — объект { [questionId]: { choice?, text?, texts?, comment? } }
  let winnerScore = 0;
  let waiterScore = 0;

  // Q5: описание продукта должности (выбор 0/1/2 как строка-индекс)
  const q5 = job[5] || job["5"];
  if (q5?.choice === "0") winnerScore += 2;       // чётко описывает продукт — Виннер
  else if (q5?.choice === "1") winnerScore += 0;  // описывает обязанности — Дуер
  else if (q5?.choice === "2") waiterScore += 2;  // не понимает продукт — Вэйтер

  // Q7: измеряет ли объём продукта (Да / В определённой мере / Нет)
  const q7 = job[7] || job["7"];
  if (q7?.choice === "Да") winnerScore += 2;
  else if (q7?.choice === "Нет") waiterScore += 2;

  // Q8: описание результатов (выбор 0/1/2)
  const q8 = job[8] || job["8"];
  if (q8?.choice === "0") winnerScore += 2;       // точные результаты
  else if (q8?.choice === "1") winnerScore += 0;  // примерно — нейтрально
  else if (q8?.choice === "2") waiterScore += 2;  // невозможно описать

  // Q9: сравнение с коллегами (первые 3 варианта = выше, последние 2 = ниже)
  const q9 = job[9] || job["9"];
  if (q9?.choice) {
    const ch = q9.choice;
    if (ch.startsWith("Мои результаты значительно выше") || ch.startsWith("Мои результаты выше") || ch.startsWith("Мои результаты немного выше")) {
      winnerScore += 1;
    } else if (ch.startsWith("Мои результаты немного ниже") || ch.startsWith("Мне трудно")) {
      waiterScore += 1;
    }
  }

  // Q10: изменение объёма ответственности (Рос / Оставался неизменным / Снижался)
  const q10 = job[10] || job["10"];
  if (q10?.choice === "Рос") winnerScore += 1;
  else if (q10?.choice === "Снижался") waiterScore += 2;

  // Q11: изменение объёма работы
  const q11 = job[11] || job["11"];
  if (q11?.choice === "Рос") winnerScore += 1;
  else if (q11?.choice === "Снижался") waiterScore += 1;

  // Q13: результаты выше/ниже плана
  const q13 = job[13] || job["13"];
  if (q13?.choice === "Выше") winnerScore += 1;
  else if (q13?.choice === "Ниже") waiterScore += 1;

  // Классификация
  const total = winnerScore + waiterScore;
  if (waiterScore >= 4) return "Вэйтер";
  if (winnerScore >= 6) return "Виннер";
  if (winnerScore >= 3) return "Дуер";
  return "Дуер"; // по умолчанию
}

export function rezultatAnalysis(result) {
  const pros = [];
  const cons = [];
  const jobs = result.jobs || [];

  const jobTypes = jobs.map((job, i) => {
    const type = classifyJob(job);
    const label = i === 0 ? "Текущее/последнее место" : `Место работы #${i + 1}`;
    return { type, label, job };
  });

  // Определяем итоговый тип по первому (текущему) месту работы
  const primaryType = jobTypes.length > 0 ? jobTypes[0].type : "Дуер";

  // Формируем pros/cons на основе ответов
  jobTypes.forEach(({ type, label, job }) => {
    const q5 = job[5] || job["5"];
    const q8 = job[8] || job["8"];
    const q10 = job[10] || job["10"];
    const q18 = job[18] || job["18"]; // достижения

    if (type === "Виннер") {
      pros.push(`${label}: кандидат чётко описывает продукт должности и результаты — признак Виннера`);
      if (q10?.choice === "Рос") pros.push(`${label}: объём ответственности рос — берёт на себя больше`);
    } else if (type === "Дуер") {
      pros.push(`${label}: кандидат работоспособен, но может нуждаться в направлении (профиль Дуера)`);
      if (q10?.choice === "Рос") pros.push(`${label}: ответственность росла — положительный сигнал`);
    } else if (type === "Вэйтер") {
      cons.push(`${label}: слабые ответы о продукте и результатах — признак Вэйтера`);
      if (q10?.choice === "Снижался") cons.push(`${label}: объём ответственности снижался — руководство не доверяло`);
    }

    // Q9: сравнение с коллегами
    const q9 = job[9] || job["9"];
    if (q9?.choice?.startsWith("Мои результаты значительно выше")) {
      pros.push(`${label}: оценивает свои результаты как значительно выше коллег`);
    } else if (q9?.choice?.startsWith("Мои результаты немного ниже") || q9?.choice?.startsWith("Мне трудно")) {
      cons.push(`${label}: затрудняется сравнить результаты с коллегами — возможно, низкие показатели`);
    }

    // Достижения (Q18 = id 18 или 19 в зависимости от структуры)
    const achieve = job[18] || job["18"] || job[19] || job["19"];
    if (achieve?.text && achieve.text.trim().length > 20) {
      pros.push(`${label}: указал конкретные достижения и предметы гордости`);
    } else if (!achieve?.text || achieve.text.trim().length < 5) {
      cons.push(`${label}: не указал достижений или предметов гордости — слабый сигнал`);
    }
  });

  // Несколько мест работы — косвенный признак нестабильности
  if (jobs.length >= 4) {
    cons.push(`Много мест работы (${jobs.length}) — возможна нестабильность`);
  }

  // Итоговый вердикт
  const typeLabel = primaryType === "Виннер"
    ? "🏆 ВИННЕР"
    : primaryType === "Дуер"
      ? "⚙️ ДУЕР"
      : "⏳ ВЭЙТЕР";

  const typeDesc = primaryType === "Виннер"
    ? "Видит проблему, находит решение и воплощает его самостоятельно. Ценный сотрудник."
    : primaryType === "Дуер"
      ? "Работоспособен, но нуждается в направлении и одобрении. Хороший исполнитель."
      : "Избегает ответственности, не торопится работать самостоятельно. Требует постоянного контроля.";

  const verdict = `${typeLabel} — ${typeDesc}`;

  return { pros, cons, verdict, primaryType };
}
