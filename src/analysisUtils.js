export function toolsAnalysis(scores) {
  const pros = [];
  const cons = [];
  const entries = Object.entries(scores);

  for (const [name, score] of entries) {
    if (score >= TOOLS_THRESHOLDS.high) {
      (TOOLS_PROS[name] || []).forEach(p => pros.push(p));
    } else if (score <= TOOLS_THRESHOLDS.low) {
      (TOOLS_CONS[name] || []).forEach(c => cons.push(c));
    }
  }

  const redFlags = entries.filter(([, s]) => s <= TOOLS_THRESHOLDS.veryLow).map(([n]) => n);
  const veryHigh = entries.filter(([, s]) => s >= TOOLS_THRESHOLDS.veryHigh).map(([n]) => n);
  const avg = entries.length ? Math.round(entries.reduce((a, [, v]) => a + v, 0) / entries.length) : 0;

  let verdict;
  if (redFlags.length >= 3) {
    verdict = `Выявлено ${redFlags.length} критически низких показателей (${redFlags.join(", ")}). Кандидат требует тщательной проверки перед наймом.`;
  } else if (avg >= 40) {
    verdict = `Средний балл +${avg}. Кандидат психологически устойчив, подходит для работы с людьми и под давлением.`;
  } else if (avg >= 0) {
    verdict = `Средний балл ${avg > 0 ? "+" : ""}${avg}. Кандидат в пределах нормы, отдельные области требуют внимания руководителя.`;
  } else {
    verdict = `Средний балл ${avg}. Психологический профиль ниже нормы — рекомендуется дополнительное собеседование.`;
  }

  return { pros, cons, verdict, redFlags, veryHigh };
}

