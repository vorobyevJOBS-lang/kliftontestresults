// 48 утверждений-маркеров (4 на тему), переформулированные как короткие предпочтения
const ITEMS = THEMES.flatMap((th) => th.q.map((text, i) => ({ theme: th.id, text, key: `${th.id}${i}` })));

// ─────────────────────────────────────────────────────────────
// ГЕНЕРАЦИЯ ПАР: каждое утверждение участвует в 4 парах,
// партнёр — из другой темы (по возможности из другого домена)
// ─────────────────────────────────────────────────────────────
function buildPairs() {
  const n = ITEMS.length; // 48
  const offsets = [11, 17]; // взаимно простые сдвиги — дают 96 пар, каждое утверждение участвует ровно в 4
  const seen = new Set();
  const pairs = [];
  offsets.forEach((off) => {
    for (let i = 0; i < n; i++) {
      const j = (i + off) % n;
      const a = Math.min(i, j), b = Math.max(i, j);
      if (a === b) continue;
      if (ITEMS[a].theme === ITEMS[b].theme) continue; // не сравниваем утверждения одной темы
      const key = `${a}-${b}`;
      if (seen.has(key)) continue;
      seen.add(key);
      pairs.push({ a: ITEMS[a], b: ITEMS[b], key: `p${a}_${b}` });
    }
  });
  return pairs;
}
const PAIRS = buildPairs();

