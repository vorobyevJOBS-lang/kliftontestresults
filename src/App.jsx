// РАСЧЁТ РЕЗУЛЬТАТОВ (ипсативный: темы соревнуются друг с другом)
// ─────────────────────────────────────────────────────────────

function computeResult(answers, positionId) {
  const pos = posById(positionId);

  // для каждой темы: сумма набранных очков + количество пар, в которых участвовала
  const raw = {};
  THEMES.forEach((th) => { raw[th.id] = { sum: 0, n: 0 }; });

  PAIRS.forEach((p) => {
    const v = answers[p.key]; // -3..-1 = A сильнее, 1..3 = B сильнее
    if (v === undefined) return;
    // вклад в тему A: если выбрано A (v<0), тема A получает |v| очков, тема B — 0 (или можно -|v|, но используем 0..6 шкалу)
    const aPts = v < 0 ? Math.abs(v) : 0; // 0..3
    const bPts = v > 0 ? v : 0; // 0..3
    raw[p.a.theme].sum += aPts; raw[p.a.theme].n += 1;
    raw[p.b.theme].sum += bPts; raw[p.b.theme].n += 1;
  });

  // средний балл по теме (0..3), затем ранжируем — это и есть ипсативный профиль
  const themeScores = THEMES.map((th) => {
    const r = raw[th.id];
    const avg = r.n > 0 ? r.sum / r.n : 1.5;
    return { id: th.id, avg, n: r.n };
  }).sort((a, b) => b.avg - a.avg);

  // ранг 1..12, и "pct" теперь — относительный балл (0-100) от мин до макс по самому профилю
  const min = Math.min(...themeScores.map((t) => t.avg));
  const max = Math.max(...themeScores.map((t) => t.avg));
  const span = max - min || 1;
  themeScores.forEach((t, i) => {
    t.rank = i + 1;
    t.pct = Math.round(((t.avg - min) / span) * 100);
  });

  const domainScores = Object.keys(DOMAINS).map((d) => {
    const ts = themeScores.filter((t) => themeById(t.id).domain === d);
    const avg = ts.reduce((a, t) => a + t.avg, 0) / ts.length;
    return { id: d, avg };
  }).sort((a, b) => b.avg - a.avg);
  // относительные проценты доменов друг к другу
  const dMin = Math.min(...domainScores.map((d) => d.avg));
  const dMax = Math.max(...domainScores.map((d) => d.avg));
  const dSpan = dMax - dMin || 1;
  domainScores.forEach((d) => { d.pct = Math.round(((d.avg - dMin) / dSpan) * 100); });

  const top5 = themeScores.slice(0, 5);
  const bottom3 = themeScores.slice(-3);

  // соответствие конкретной должности — по рангу ключевых тем (а не по абсолютному %)
  const keys = pos.keyThemes;
  const keyScores = keys.map((k) => themeScores.find((t) => t.id === k));
  const avgRank = keyScores.reduce((a, t) => a + t.rank, 0) / keyScores.length; // 1 (лучший) .. 12 (худший)
  const keyAvg = Math.round(((12 - avgRank) / 11) * 100); // переводим средний ранг в условный %, для совместимости с текстом
  const weakKeys = keyScores.filter((t) => t.rank >= 8).sort((a, b) => b.rank - a.rank); // в нижней половине профиля

  let fit, fitNote;
  if (avgRank <= 4 && weakKeys.length === 0) {
    fit = "Высокое соответствие должности";
    fitNote = `Ключевые для роли «${pos.name}» качества в среднем занимают ${avgRank.toFixed(1)}-е место в профиле кандидата (из 12) — то есть входят в число его наиболее выраженных сторон.`;
  } else if (avgRank <= 7 && weakKeys.length <= 1) {
    fit = "Среднее соответствие должности";
    fitNote = `Ключевые для роли «${pos.name}» качества в среднем занимают ${avgRank.toFixed(1)}-е место в профиле (из 12) — часть из них выражена сильно, часть — нет. Решение стоит принимать по итогам собеседования (см. вопросы ниже).`;
  } else {
    fit = "Низкое соответствие должности";
    fitNote = `Ключевые для роли «${pos.name}» качества в среднем занимают ${avgRank.toFixed(1)}-е место в профиле (из 12) — то есть относятся к менее выраженным сторонам кандидата. Это не значит, что кандидат «плохой» — посмотрите его топ-5 ниже, возможно ему ближе другая роль.`;
  }

  // точечные вопросы: сначала по слабым ключевым темам должности, затем по общим bottom3
  const askedThemes = new Set();
  const targetedQuestions = [];
  weakKeys.forEach((t) => {
    if (pos.questions[t.id]) { targetedQuestions.push({ theme: t.id, q: pos.questions[t.id], pct: t.pct, rank: t.rank }); askedThemes.add(t.id); }
  });
  bottom3.forEach((t) => {
    if (!askedThemes.has(t.id) && pos.questions[t.id]) {
      targetedQuestions.push({ theme: t.id, q: pos.questions[t.id], pct: t.pct, rank: t.rank }); askedThemes.add(t.id);
    }
  });
  if (targetedQuestions.length < 3) {
    top5.forEach((t) => {
      if (targetedQuestions.length >= 4) return;
      if (!askedThemes.has(t.id)) {
        targetedQuestions.push({ theme: t.id, q: themeById(t.id).iq, pct: t.pct, rank: t.rank });
        askedThemes.add(t.id);
      }
    });
  }

  // ── СВОДКА ДЛЯ РУКОВОДИТЕЛЯ ──
  // подводные камни и точки наблюдения — из топ-5 (это и есть доминирующее поведение) + слабых ключевых для должности тем
  const summarySourceIds = [...new Set([...top5.map((t) => t.id), ...weakKeys.map((t) => t.id)])];
  const pitfalls = summarySourceIds
    .filter((id) => themeById(id).pitfall)
    .slice(0, 5)
    .map((id) => ({ theme: id, rank: themeScores.find((t) => t.id === id).rank, text: themeById(id).pitfall }));
  const watchpoints = summarySourceIds
    .filter((id) => themeById(id).watch)
    .slice(0, 5)
    .map((id) => ({ theme: id, rank: themeScores.find((t) => t.id === id).rank, text: themeById(id).watch }));

  const top3names = top5.slice(0, 3).map((t) => themeById(t.id).name.toLowerCase());
  const portrait = `Доминирующие качества кандидата — ${top3names.join(", ")}. ${themeById(top5[0].id).desc} В сочетании с ${top3names[1]} и ${top3names[2]} это формирует основной стиль работы человека: на это стоит опираться при постановке задач и распределении ролей в команде.`;

  return { themeScores, domainScores, top5, bottom3, keyAvg, avgRank, weakKeys, fit, fitNote, targetedQuestions, position: pos, pitfalls, watchpoints, portrait };
}

